/**
 * An AdTier represents the level of prominance of an advrtisement
 */
export enum AdTier {

    /** Offers the most basic level of advertisement */
    Classic = 0,

    /** Allows advertisers to use a company logo and use a longer
presentation text */
    StandOut,

    /** Same benefits as Standout Ad, but also puts the advertisement at
the top of the results, allowing higher visibility */
    Premium,

}
